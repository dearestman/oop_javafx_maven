package com.company.domain.users;

public class BankAccount {
    private String numberBankAccount;
    private double balanceBankAccount;

    public String getNumberBankAccount() {
        return numberBankAccount;
    }

    public void setNumberBankAccount(String numberBankAccount) {
        this.numberBankAccount = numberBankAccount;
    }

    public double getBalanceBankAccount() {
        return balanceBankAccount;
    }

    public void setBalanceBankAccount(double balanceBankAccount) {
        this.balanceBankAccount = balanceBankAccount;
    }
}
